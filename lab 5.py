# 3) Функция для изменения местами первого и последнего элемента
def change(lst):
    if len(lst) >= 2:
        lst[0], lst[-1] = lst[-1], lst[0]

lst = [1, 2, 3, 4, 5]
change(lst)
print("Измененный список:", lst)
